/// HKDF (RFC 5869, HMAC-SHA-256) known-answer vector.
///
/// Exercises HKDF-Extract (`salt` + `ikm` → PRK) followed by HKDF-Expand
/// (PRK + `info` → OKM), comparing the derived `okm` against the expected
/// vector. Byte-for-byte identical to the reference firmware's
/// `HKDF_SELF_TEST_256_{SALT,IKM,INFO,DKM}`.
pub(super) struct HkdfKat {
    /// Salt (HKDF-Extract).
    pub salt: &'static [u8],

    /// Input keying material (HKDF-Extract).
    pub ikm: &'static [u8],

    /// Context/application info (HKDF-Expand): PartyU ‖ PartyV ‖ length.
    pub info: &'static [u8],

    /// Expected derived keying material (output of HKDF-Expand).
    pub okm: &'static [u8],
}


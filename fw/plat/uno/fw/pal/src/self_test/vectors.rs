/// KBKDF (SP 800-108 counter mode, HMAC-SHA-512) known-answer vector.
///
/// Exercises the production [`sp800_108_kdf`](azihsm_fw_hsm_pal_traits::HsmKdf::sp800_108_kdf)
/// path, which builds each PRF input as `counter â€– label â€– 0x00 â€– context â€– L`
/// and derives `output = HMAC-SHA-512(key, â€¦)`.
///
/// The reference firmware's KBKDF self-test (and the raw NIST CAVP counter-mode
/// vectors) use an **opaque `FixedInputData`** blob, which uno's structured KDF
/// cannot reproduce (it always inserts the `0x00` separator and appends its own
/// `L`). So this `okm` was computed for uno's exact construction with a
/// KDF-CTR implementation **validated to reproduce the NIST CAVP HMAC-SHA-512
/// answers byte-for-byte**; `key` is a real NIST CAVP HMAC-SHA-512 key.
pub(super) struct KbkdfKat {
    /// Key-derivation key (KDK), 64 bytes (NIST CAVP HMAC-SHA-512 key).
    pub key: &'static [u8],

    /// Label (application-specific purpose string).
    pub label: &'static [u8],

    /// Context (application-specific context bytes).
    pub context: &'static [u8],

    /// Expected derived output (40 bytes; L = 320 bits, single SHA-512 block).
    pub okm: &'static [u8],
}


/// Returns the (Gx, Gy) generator coordinates for the requested NIST curve.
pub(crate) fn generator(curve: EccCurve) -> (Vec<u8>, Vec<u8>) {
    match curve {
        EccCurve::P256 => (P256_GX.to_vec(), P256_GY.to_vec()),
        EccCurve::P384 => (P384_GX.to_vec(), P384_GY.to_vec()),
        EccCurve::P521 => (P521_GX.to_vec(), P521_GY.to_vec()),
    }
}

/// Computes `p - y` (point negation in the Y coordinate) for the requested
/// NIST curve. Used by tests that need `-G = (Gx, p - Gy)`.
pub(crate) fn negate_y_modp(curve: EccCurve, y: &[u8]) -> Vec<u8> {
    const P256_P: [u8; 32] = [
        0xff, 0xff, 0xff, 0xff, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
        0xff, 0xff,
    ];
    const P384_P: [u8; 48] = [
        0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
        0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
        0xff, 0xfe, 0xff, 0xff, 0xff, 0xff, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xff,
        0xff, 0xff, 0xff,
    ];
    const P521_P: [u8; 66] = [
        0x01, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
        0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
        0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
        0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
        0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
    ];
    let p: &[u8] = match curve {
        EccCurve::P256 => &P256_P,
        EccCurve::P384 => &P384_P,
        EccCurve::P521 => &P521_P,
    };
    assert_eq!(p.len(), y.len());
    let mut out = vec![0u8; p.len()];
    let mut borrow = 0i16;
    for i in (0..p.len()).rev() {
        let v = p[i] as i16 - y[i] as i16 - borrow;
        if v < 0 {
            out[i] = (v + 256) as u8;
            borrow = 1;
        } else {
            out[i] = v as u8;
            borrow = 0;
        }
    }
    out
}
